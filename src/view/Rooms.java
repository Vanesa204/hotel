    private void buttEkiminarActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_buttEkiminarActionPerformed
     
        int selectedRow = tableRoom.getSelectedRow();
         if (selectedRow == -1) {
        JOptionPane.showMessageDialog(this, "Seleccione una habitacion para eliminar.");
        return;
        
         
    }
         //Object id = tableRoom.getValueAt(selectedRow, 0);
         
    int id = (int) tableRoom.getValueAt(selectedRow, 0);
           model.Room r = new model.Room();
           
            r.setId(id);
            dao.delete(r);
            
              // Eliminar la fila del modelo de la tabla
         DefaultTableModel model = (DefaultTableModel) tableRoom.getModel();
         model.removeRow(selectedRow);
         
          ListaHabitaciones();
           JOptionPane.showMessageDialog(this, "Habitación eliminada exitosamente.");
    }//GEN-LAST:event_buttEkiminarActionPerformed

    
    
    
    
    
    
    
    
    
    
    
    
    
    
    
    
    

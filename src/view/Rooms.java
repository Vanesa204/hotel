    private void buttAgregarActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_buttAgregarActionPerformed
     
 try {
        int nHab = Integer.parseInt(texHabitacionN.getText());
        int sim = Integer.parseInt(jComboBoxSimple.getSelectedItem().toString());
         int dob = Integer.parseInt(jComboBoxDoble.getSelectedItem().toString());
         double prec = Double.parseDouble(texPrecio.getText());
    
          if (dao.existsByRoomNumber(nHab)) {
        // Mostrar el mensaje al usuario en la ventana actual
        JOptionPane.showMessageDialog(this, "La habitación ya existe. No se puede insertar duplicado.", "Error", JOptionPane.ERROR_MESSAGE);
        return;
    }      
            if (nHab == 0 || prec == 0) {
            throw new IllegalArgumentException("Todos los campos deben estar llenos.");
        }
        model.Room r = new model.Room();
   
       r.setnHabitacion(nHab);
       r.setSimple(sim);
       r.setDoble(dob);
       r.setPrecio(prec);
       
        dao.save(r);
        ListaHabitaciones();
        DefaultTableModel model = (DefaultTableModel)tableRoom.getModel();
        
       model.insertRow(0,new Object[]{r.getId(), r.getnHabitacion(),r.getSimple(),r.getDoble(),r.getPrecio()});
       ListaHabitaciones();   

          
        JOptionPane.showMessageDialog(this, "Habitación guardada exitosamente.");
    } catch (NumberFormatException e) {
        JOptionPane.showMessageDialog(this, "Por favor, ingrese valores numéricos válidos.", "Error", JOptionPane.ERROR_MESSAGE);
    } 
        
          
    }//GEN-LAST:event_buttAgregarActionPerformed

